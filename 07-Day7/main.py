#Arithmetic Operator
print(3+4)  #addition
print(6-3)  #subtraction
print(2*4)  #multiplication
print(4%2)  #modulus
print(4//2) #integer division
print(4/2)  #float division
print(2**2) #power

#Comparison Operator
print(3>8)
print(3<8)
print(3==4)
print(3!=8)
print(3<=3)
print(4>=5)

#Logical Operator
print(1<3 and 4<7)  #it will give True/False
print(1 or 1)       #it will give 1/0
print(1 or 0)
print(not 1==1)

